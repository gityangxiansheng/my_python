name=["h郑州","k开封","j西安","i许昌","h扬州","g广州","f重庆","e太原","d烟台","c海南","b中南海","a延安"]
print(name)
print("按原始排列顺序打印该列表。不要考虑输出是否整洁的问题，只管打印原始Python列表。")

print(sorted(name))
print("使用sorted() 按字母顺序打印这个列表，同时不要修改它。")

print(name)
print("再次打印该列表，核实排列顺序未变。")
print(sorted(name,reverse=True))
print("使用sorted() 按与字母顺序相反的顺序打印这个列表，同时不要修改它。")

print(name)
print("再次打印该列表，核实排列顺序未变。")

name.reverse()
print(name)
print("使用reverse() 修改列表元素的排列顺序。打印该列表，核实排列顺序确实变了。")

name.reverse()
print(name)
print("再次修改列表元素的排列顺序。打印该列表，核实已恢复到原来的排列顺序。")

